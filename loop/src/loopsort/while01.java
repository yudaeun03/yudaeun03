package loopsort;

public class while01 {
	public static void main (String[] args) {
		
		// while 반복문
		// 횟수를 모를 때 사용
		// 종료 시점을 알 때 사용
		
		int n = 1;
		while (n <= 10) {
			System.out.println("while = " + n);
			n++;
		}
		
		
		// for 구문이라면 이렇게 작성
		// -> for(int i=1; i<10; i++) {
		//	System.out.println("for =" + i);
	}

}
